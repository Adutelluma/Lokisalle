	<div style="font-size:25px; color:grey; margin-top:50px"> La communication des conditions générales de vente</div>
		<p>L'information précontractuelle est organisée par le II de l'article L. 441-1 du Code de commerce qui fait obligation à toute personne exerçant des activités de production, de distribution ou de services qui établit des conditions générales de vente de les communiquer à tout acheteur qui en fait la demande pour une activité professionnelle. Cette communication s'effectue par tout moyen constituant un support durable.</p>
		<p>Les conditions générales de vente peuvent être différenciées selon les catégories d'acheteurs de produits ou de demandeurs de prestation de services (par exemple, détaillants, grossistes). Dans ce cas, l'obligation de communication ne s'applique qu'à l'égard des acheteurs de produits ou des demandeurs de prestations de services d'une même catégorie. Les conditions générales/div de vente constituant le socle unique de la négociation commerciale, tout producteur, prestataire de services, grossiste ou importateur peut, par ailleurs, dans le cadre de cette négociation convenir avec un acheteur de produits ou un demandeur de prestation de services des conditions particulières de vente qui ne sont pas soumises à cette obligation de communication.</p>
		<p>En application du IV de l’article L. 441-1 du Code de commerce, le refus de communiquer des CGV existantes à tout acheteur qui en fait la demande pour une activité professionnelle est passible d’une amende administrative d’un montant maximal de 15°000 € pour une personne physique et de 75 000 € pour une personne morale. </p>
